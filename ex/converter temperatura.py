''' Faça um programa que transforme uma temperatura fornecida em Celsius para a correspondente em Fahrenheit. 
A fórmula de conversão de Celsius para Fahrenheit é a seguinte: C = (5/9) * (F –32). '''

tempF = float(input("digite a temperatura que deseja converter"))

C = (5/9) * (tempF - 32)

print(C)
